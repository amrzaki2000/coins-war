mov cx,player2x
mov dx, player2y

add cx, p2w
add dx, p2H
lea di, p2
jmp loop2

draw2:
mov ah,0ch
mov al, [di]
mov bh, 00h

cmp al,0
jz loop2
int 10h


loop2:
inc di
dec cx
cmp cx,player2x
jnz draw2

add cx, p2w
dec dx
cmp dx,player2y
jz terminate2
jnz draw2

terminate2:
RET
DrawPlayer2 ENDP

