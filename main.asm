getinput proc near
push ax 
push bx
mov ah,01h
int 16h

cmp ax, Wkey
jz up

cmp ax, Skey
jz down

cmp ax, Dkey
jz right

cmp ax, Akey
jz left
jmp freeze

up:
call flushkeybuffer
mov al, updirection
mov input, al
jmp nextinput1

down:
call flushkeybuffer
mov al, downdirection
mov input, al
jmp nextinput1

right:
call flushkeybuffer
mov al, rightdirection
mov input, al
jmp nextinput1

left:
call flushkeybuffer
mov al, leftdirection
mov input, al
jmp nextinput1

freeze:
mov bl, nomove
mov input, bl
jmp nextinput1

